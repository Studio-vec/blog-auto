def _current_season() -> str:
    m = date.today().month
    if m in (3, 4, 5):
        return "봄"
    if m in (6, 7, 8):
        return "여름"
    if m in (9, 10, 11):
        return "가을"
    return "겨울"


def pick_topic() -> dict:
    """현재 계절에 맞는 여행지를 우선 선택."""
    season = _current_season()

    seasonal = [d for d in DOMESTIC if season in d["season"] or "사계절" in d["season"]]
    pool = seasonal if seasonal else DOMESTIC

    dest = random.choice(pool)
    category = random.choice(CATEGORIES)

